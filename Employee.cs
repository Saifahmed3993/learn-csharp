namespace Field_Constant008
{

    //<Class Modifier> class <Class Name>

    public class Employee
    {
        //<Access Modifier> : public , private, protected, internal, protected internal, private protected

        //field --> <Access Modifier> <Data Type> <Field Name> = <Value>;
        public string FirstName;
        public string LastName;
        public double Wage;
        public double LoggedHour;

        //constant --> <Access Modifier> const <Data Type> <Constant Name> = <Value>;
        public const double TaxRate = 0.03; // Fixed tax rate of 3%




    }
}
